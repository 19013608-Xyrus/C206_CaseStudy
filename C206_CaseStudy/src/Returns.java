public class Returns {
		private int returns;
		
		public Returns(int returns) {
			super();
			this.returns = returns; 
		}
		
		public int getReturns() {
			return returns;
		}
		
		public String toString() {
			String output = String.format("%s", returns);
			return output;
	}
	
}

